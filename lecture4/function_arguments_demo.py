def add(a, b, c):
    return a + b + c


# Unpack a list/tuple with *
values = [1, 2, 3]
print(add(*values))  # print(add(1,2,3)) -> 6

# Unpack a dictionary with **
params = {"a": 10, "b": 20, "c": 30}
print(add(**params))  # print(add(a=10, b=20, c=30)) -> 60
